#include "twl_lst.h"

void				twl_lst_push_front(t_lst *lst, void *data)
{
	t_lst_elem__ *elem;

	elem = twl_lst_create_elem__(data);
	twl_lst_link__(elem, lst->head);
	lst->head = elem;
}
