#include "twl_lst.h"

static void			iter_fn(void *data, void *dst)
{
	twl_lst_push_back(dst, data);
}

void				twl_lst_extend(t_lst *dst, t_lst *src)
{
	twl_lst_iter(src, iter_fn, dst);
}
