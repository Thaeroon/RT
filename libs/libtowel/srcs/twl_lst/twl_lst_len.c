#include "twl_lst.h"

size_t	twl_lst_len(t_lst *lst)
{
	int				len;
	t_lst_elem__	*elem;

	elem = lst->head;
	len = 0;
	while (elem)
	{
		elem = elem->next;
		len++;
	}
	return (len);
}
