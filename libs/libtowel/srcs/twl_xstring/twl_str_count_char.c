#include <stdlib.h>
#include "twl_xstring.h"

unsigned int		twl_str_count_char(char *s, char c)
{
	unsigned int	count;

	count = 0;
	while (*s)
	{
		if (*s == c)
			count++;
		s++;
	}
	return (count);
}
