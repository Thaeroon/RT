#include <stdlib.h>
#include "twl_xstring.h"

char	*twl_str_replace_free(char *s, char *target, char *replacement)
{
	char *replaced_str;

	replaced_str = twl_str_replace(s, target, replacement);
	free(s);
	return (replaced_str);
}
