#include <stdlib.h>
#include "twl_string.h"
#include "twl_xstring.h"

char				*twl_strndup(const char *src, int n)
{
	char			*s;

	s = twl_strnew(n);
	twl_strncpy(s, src, n);
	return (s);
}
