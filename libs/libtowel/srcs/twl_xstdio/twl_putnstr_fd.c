#include <unistd.h>
#include "twl_string.h"

/*
** print string 's' to file descriptor 'fd'
*/

void	twl_putnstr_fd(char const *s, int n, int fd)
{
	while (n--)
		write(fd, s, twl_strlen(s));
}
