#include <stdlib.h>
#include "twl_string.h"

char	*twl_strdup(const char *s1)
{
	char *copy;

	if (!(copy = (char *)malloc(sizeof(char) * (twl_strlen(s1) + 1))))
		return (NULL);
	twl_strcpy(copy, s1);
	return (copy);
}
