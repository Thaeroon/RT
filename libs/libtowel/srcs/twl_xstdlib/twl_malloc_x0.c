#include "twl_xstdlib.h"
#include "twl_string.h"

void	*twl_malloc_x0(int size)
{
	void *new_malloc;

	new_malloc = twl_xmalloc(size);
	twl_bzero(new_malloc, size);
	return (new_malloc);
}
